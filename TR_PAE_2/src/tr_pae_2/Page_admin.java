    private void load_stok_barang(String cari) {
        try {
            String sql = "SELECT t.Nama_Toko, s.Id_Toko, s.Id_Barang, s.QTY_barang "
                    + "FROM stock s INNER JOIN toko t "
                    + "ON t.Id_Toko = s.Id_Toko "
                    + "WHERE t.Nama_Toko LIKE '%" + cari + "%'";
            PreparedStatement stm = connection.prepareStatement(sql);
            ResultSet rs = stm.executeQuery();
            List<Object[]> record = new ArrayList<Object[]>();
            while (rs.next()) {
                Object data[] = new Object[4];
                data[0] = rs.getString(1);
                data[1] = rs.getString(2);
                data[2] = rs.getString(3);
                data[3] = rs.getInt(4);
                record.add(data);
            }
            Object tampung[][] = new Object[record.size()][5];
            int index = 0;
            for (Object[] hasil : record) {
                tampung[index] = hasil;
                index++;
            }
            String header[] = {"Nama Toko", "Id Toko", "Id Barang", "Qty Barang"};
            DefaultTableModel mode = new DefaultTableModel(tampung, header);
            table_stok_barang.setModel(mode);

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

