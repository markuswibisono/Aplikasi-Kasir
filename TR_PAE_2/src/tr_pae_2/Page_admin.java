    private void btn_barang_submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_barang_submitActionPerformed
        if (btn_barang_submit.getText().equals("Submit")) {
            try {
                String sql = "INSERT INTO barang(Id_Barang, Nama_Barang, Harga, Group_Id) VALUES(?,?,?,?)";
                PreparedStatement stm = connection.prepareStatement(sql);
                stm.setString(1, text_barang_IdBarang.getText());
                stm.setString(2, text_barang_NamaBarang.getText());

                int harga = Integer.parseInt(text_barang_HargaBarang.getText());
                stm.setInt(3, harga);
                stm.setString(4, cbox_barang_groupBarang.getSelectedItem().toString());

                if (stm.executeUpdate() > 0) {
                    JOptionPane.showMessageDialog(this, "Berhasil diInput !!!!");

                }
                Load_DataBarang("");
            } catch (Exception e) {
                e.printStackTrace();
            }
        } else if (btn_barang_submit.getText().equals("Save")) {
            try {
                String Id_Barang = text_barang_IdBarang.getText();
                String Nama_Barang = text_barang_NamaBarang.getText();
                int harga = Integer.parseInt(text_barang_HargaBarang.getText());

                String sql = "UPDATE barang "
                        + "SET Nama_Barang='" + Nama_Barang + "',Harga='" + harga + "' "
                        + "WHERE Id_Barang='" + Id_Barang + "'";
                PreparedStatement stm = connection.prepareStatement(sql);
                if (stm.executeUpdate() > 0) {
                    System.out.println("berhasil update");
                    Load_DataBarang("");
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        } else {
            System.out.println("PERINGATAN!!! :: gagal update/insert data barang ::");
        }

        clearTeks();
    }//GEN-LAST:event_btn_barang_submitActionPerformed

