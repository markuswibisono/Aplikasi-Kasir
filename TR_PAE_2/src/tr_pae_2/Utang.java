    private void tampung_banyak_no() {
        try {
            String sql = "SELECT Id_Utang FROM utang";
            PreparedStatement stm = connection.prepareStatement(sql);
            ResultSet rs = stm.executeQuery();
            int tampung = 0;
            while (rs.next()) {
                tampung++;
            }
            System.out.println(tampung + " no trans");
            gsUtang.setTampung_no(tampung);
        } catch (SQLException ex) {
            Logger.getLogger(Utang.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
