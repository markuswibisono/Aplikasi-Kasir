    private int cek_id_pelanggan(String id) throws SQLException {
        String query = "SELECT Id_Pelanggan FROM pelanggan WHERE Id_Pelanggan = '" + id + "'";
        PreparedStatement stm = connection.prepareStatement(query);
        ResultSet rs = stm.executeQuery();
        int cek = 0;
        while (rs.next()) {
            cek++;
        }
        return cek;
    }
    
