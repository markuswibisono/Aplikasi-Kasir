    private void btn_utangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_utangActionPerformed
        String id_pelanggan = txt_id_pelanggan.getText();
        
        try {
            if (id_pelanggan.equals("") || txt_tagihan.getText().equals("") || txt_total_cicilan.getText().equals("") || txt_total_harga.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Ada Field yang kosong");
            } else if (cek_id_pelanggan(id_pelanggan) < 1) {
                JOptionPane.showMessageDialog(this, "ID Pelanggan Anda salah :p");
            } else {
                int cicilan = Integer.parseInt(txt_total_cicilan.getText());
                int total_harga = Integer.parseInt(txt_total_harga.getText());
                String sql = "INSERT INTO utang(Id_Utang, Id_Pelanggan, Id_Transaksi, Cicilan, Total_Harga_Transaksi, Sisa_kali_cicilan) VALUES (?,?,?,?,?,?)";
                PreparedStatement stm = connection.prepareStatement(sql);
                stm.setString(1, "Utg" + (gsUtang.getTampung_no() + 1));
                stm.setString(2, id_pelanggan);
                stm.setString(3, id_trans_pub);
                stm.setInt(4, cicilan);
                stm.setInt(5, total_harga);
                stm.setInt(6, (cicilan - 1));
                
                if (stm.executeUpdate() > 0) {
                    System.out.println("berhasil");
                    JOptionPane.showMessageDialog(this, "Cicilan Berhasil");
                }
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btn_utangActionPerformed
    
