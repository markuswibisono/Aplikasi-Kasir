public class GetSetTrans {
    private int tampung_no;
    private int tampung_id_trans;
    private String username;
    private String pelanggan;
    private String id_trans;
    
    /**
     * @return the tampung_no
     */
    public int getTampung_no() {
        return tampung_no;
    }

    /**
     * @param tampung_no the tampung_no to set
     */
    public void setTampung_no(int tampung_no) {
        this.tampung_no = tampung_no;
    }

    /**
     * @return the tampung_id_trans
     */
    public int getTampung_id_trans() {
        return tampung_id_trans;
    }

    /**
     * @param tampung_id_trans the tampung_id_trans to set
     */
    public void setTampung_id_trans(int tampung_id_trans) {
        this.tampung_id_trans = tampung_id_trans;
    }

    /**
     * @return the username
     */
    public String getUsername() {
        return username;
    }

    /**
     * @param username the username to set
     */
    public void setUsername(String username) {
        this.username = username;
    }

    /**
     * @return the pelanggan
     */
    public String getPelanggan() {
        return pelanggan;
    }

    /**
     * @param pelanggan the pelanggan to set
     */
    public void setPelanggan(String pelanggan) {
        this.pelanggan = pelanggan;
    }

    /**
     * @return the id_trans
     */
    public String getId_trans() {
        return id_trans;
    }

    /**
     * @param id_trans the id_trans to set
     */
    public void setId_trans(String id_trans) {
        this.id_trans = id_trans;
    }
}
